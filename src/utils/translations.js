// Функция для получения текста по ключу с заменой переменных
export const t = (key, language = 'ru', variables = {}) => {
  const text = translations[language]?.[key] || translations.ru[key] || key;
  
  // Заменяем переменные в тексте
  return Object.keys(variables).reduce((result, variable) => {
    return result.replace(`{${variable}}`, variables[variable]);
  }, text);
};

// Функция для получения языка из localStorage или установки по умолчанию
export const getStoredLanguage = () => {
  try {
    return localStorage.getItem('selectedLanguage') || 'ru';
  } catch (error) {
    return 'ru';
  }
};

// Функция для сохранения языка в localStorage
export const saveLanguage = (language) => {
  try {
    localStorage.setItem('selectedLanguage', language);
    return true;
  } catch (error) {
    console.error('Ошибка сохранения языка:', error);
    return false;
  }
}; 